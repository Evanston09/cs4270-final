package me.evankim.cs4270final;

import javafx.application.Application;

/**
 * Launcher class for the FLL RefBot Scoresheet Scanner application.
 * This is the entry point that launches the JavaFX application.
 */
public class Launcher {
    /**
     * Main entry point for the application.
     * Launches the FLLRefBotApplication JavaFX application.
     *
     * @param args command-line arguments passed to the application
     */
    public static void main(String[] args) {
        Application.launch(FLLRefBotApplication.class, args);
    }
}
